#pragma once

/**
 * @brief Key codes for user input
 * 
 * Defines the standard key codes used throughout the library.
 * ASCII codes are used for numeric keys (0x30-0x39).
 */
enum Keys
{
    KEY_MENU = 1,       ///< Menu key
    KEY_MODE,           ///< Mode key
    KEY_ENTER,          ///< Enter key
    KEY_ESCAPE,         ///< Escape key
    KEY_DELETE,         ///< Delete key
    KEY_BACKSPACE,      ///< Backspace key
    KEY_DOWN,           ///< Down arrow
    KEY_UP,             ///< Up arrow
    KEY_LEFT,           ///< Left arrow
    KEY_RIGHT,          ///< Right arrow
    KEY_PAGEUP,         ///< Page Up key
    KEY_PAGEDOWN,       ///< Page Down key
    KEY_PLUS,           ///< Plus key
    KEY_MINUS,          ///< Minus key
    KEY_0 = 0x30,       ///< Numeric key 0
    KEY_1,              ///< Numeric key 1
    KEY_2,              ///< Numeric key 2
    KEY_3,              ///< Numeric key 3
    KEY_4,              ///< Numeric key 4
    KEY_5,              ///< Numeric key 5
    KEY_6,              ///< Numeric key 6
    KEY_7,              ///< Numeric key 7
    KEY_8,              ///< Numeric key 8
    KEY_9               ///< Numeric key 9
};
